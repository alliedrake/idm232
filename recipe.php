<div class="text-container">

<div class="ingredients">
    <h2>Ingredients</h2>
    <hr>

<div class="ingredientlist">
    <ul>
        <li>1 serving olive oil cooking spray</li>
        <li>1 acorn squash, halved and seeded</li>
        <li>1 tablespoon olive oil</li>
        <li>1 pinch salt and freshly ground black pepper to taste</li>
        <li>1/2 pound sage-flavored ground breakfast sausage</li>
        <li>1/2 onion, diced</li>
        <li>1/2 cup thinly sliced cabbage</li>
        <li>1/2 cup matchstick-cut carrots</li>
        <li>1 pear - peeled, cored, and diced</li>
    </ul>
</div>
</div>

<div class="directions">
    <h2>Directions</h2>
    <hr>

    <h3>Step 1</h3>
    <p>Preheat the oven to 400 degrees F (200 degrees C). Line a baking pan with aluminum foil and spray with oil.</p>
    
    <h3>Step 2</h3>
    <p>Rub or brush 1/2 tablespoon oil on the flesh of each acorn squash half. Season with salt and pepper.Place squash halves onto the prepared baking pan, cut-side down.</p>

    <h3>Step 3</h3>
    <p>Roast in the preheated oven until softened, about 30 minutes.</p>

    <h3>Step 4</h3>
    <p>Meanwhile, brown sausage in a large skillet over medium heat. Add onion, cabbage, and carrots. Saute until onion is soft and translucent, 5 to 7 minutes. Add diced pear and cook until softened but still crunchy, 2 to 3 minutes longer.</p>

    <h3>Step 5</h3>
    <p>Remove acorn squash from oven and turn over using tongs. Stuff with sausage-pear mixture. Return to oven and roast for an additional 10 minutes.</p>
</div>

